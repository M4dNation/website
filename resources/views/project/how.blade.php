				<h1>How it works ?</h1>
				<p>The principle is simple: you put a <b>game idea</b> on the platform, and all other users can react in order to <b>modify or improve it.</b> </p>
				<p>Each <b>game idea</b> presented on the platform is thus considered as a <b>project</b>.</p>
				<p>All projects on Yggdrasill work in three phases: <b>design, production and diffusion.</b></p>
				<p>During <b>the design phase</b>, all the users help creating <b>a tree</b> by discussing the <b>game idea</b>, modifying or improving it. The <b>tree</b> is a graphical representation of a <b>game project</b>, and contains all the ideas submitted.</p>
				<p>When a <b>tree</b> reaches maturity, it is frozen, giving <b>the final representation</b> of the game project in the form of a <b>detailed specification</b> ready to be produced.</p>
				<p>If the original idea came from <b>an indie studio</b>, it may decide to start <b>the production phase</b> and begin development.</p>
				<p>If it came from <b>a player</b>, we will find a studio willing to carry out <b>the production phase </b>of the game in accordance with <b>your expectations.</b></p>
				<p>When <b>the first release</b> is available, the project is finally in <b>the diffusion phase</b>, in which you can start <b>promoting the game you help created</b> around you and gather more players until <b>the final release.</b></p>
